$(document).ready(function () {
    // Get today's date
    var today = new Date();

    // Initialize the date picker with endDate attribute
    $('#datepicker1').datepicker({
        autoclose: true,
        endDate: today
    });

    // Show the datepicker when the calendar icon is clicked
    $('#datepicker-icon1').on('click', function () {
        $('#datepicker1').datepicker('show');
    });
});


